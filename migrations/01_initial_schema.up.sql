CREATE TABLE ENTRY (
       ACCESS_CODE VARCHAR2(128) NOT NULL,
       REF_ACCESS_CODE VARCHAR2(128) NOT NULL,
       RECORD INTEGER,
       COMMENT VARCHAR2(256),

       CONSTRAINT ENTRY_PK PRIMARY KEY (ACCESS_CODE, REF_ACCESS_CODE)
);

CREATE TABLE MOOD (
       ACCESS_CODE VARCHAR2(128) NOT NULL PRIMARY KEY,
       TITLE VARCHAR2(128) NOT NULL,
       CONTENT VARCHAR2(512) NOT NULL,
       NUMBER_OF_RECORDS_NEEDED INTEGER NOT NULL
);
